    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int index = tableMakanan.getSelectedRow();
        if (index != -1) {
            HargaKacamata m = tableModelKacamata.getKacamata(tableMakanan.convertRowIndexToModel(index));
            if (JOptionPane.showConfirmDialog(null, "Apakah Yakin Di Hapus?", "konfirmasi", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
                try {
                    sm.deleteKacamata(m);
                    tableModelKacamata.deleteKacamata(index);
                    refresh();
                    JOptionPane.showMessageDialog(rootPane, "Data berhasil dihapus!");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int index = tableMakanan.getSelectedRow();
        if (index != -1) {
            HargaKacamata m = tableModelKacamata.getKacamata(tableMakanan.convertRowIndexToModel(index));

            String kode = txtkode.getText();
            String nama = txtNama.getText();
            String harga = txtharga.getText();

            m.setNama(nama);
            m.setHarga(harga);
            m.setKode(kode);

            try {
                sm.updateKacamata(m);
                tableModelKacamata.updateKacamata(index, m);
                refresh();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertActionPerformed
        // TODO add your handling code here:
        String kode = txtkode.getText();
        String nama = txtNama.getText();
        String harga = txtharga.getText();

        HargaKacamata m = new HargaKacamata();
        m.setKode(kode);
        m.setNama(nama);
        m.setHarga(harga);

        try {
            sm.insertKacamata(m);
            tableModelKacamata.insertKacamata(m);
            refresh();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, ex.getMessage());
        }
    }//GEN-LAST:event_btnInsertActionPerformed

