    private void printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printActionPerformed

        //((Mahasiswa)mahasiswa).showMahasiswa();
        if (txtkode.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Data masing kosong!");
        } else {
            PrintJob p = getToolkit().getPrintJob(this, "cetak", null);

            Graphics g = p.getGraphics();
            g.setFont(new Font("Serif", Font.BOLD, 20));
            g.drawString("DAFTAR HARGA KACAMATA ", 150, 18);
            g.drawString("Kode: "+txtkode.getText(), 250, 40);
            g.drawLine(10, 55, 600, 55);
            g.setFont(new Font("Courier New", Font.PLAIN, 12));
            g.drawString("Nama", 10, 70);
            g.drawString(" : ", 100, 70);
            g.drawString(txtNama.getText(), 160, 70);

            g.drawString("Harga", 10, 80);
            g.drawString(" : ", 100, 80);
            g.drawString(txtharga.getText(), 160, 80);
            p.end();
        }
    }//GEN-LAST:event_printActionPerformed

